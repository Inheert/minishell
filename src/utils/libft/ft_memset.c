#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*_ptr;

	if (!s)
		return (NULL);
	_ptr = (unsigned char *)s;
	while (n--)
	{
		*_ptr++ = c;
	}
	return (s);
}
