#include "minishell.h"

void	ft_env(char **menvp)
{
	int	i;

	if (!menvp || !*menvp)
		return ;
	i = -1;
	while (menvp[++i])
	{
		ft_putstr_fd(menvp[i], 1);
		write(1, "\n", 1);
	}
}
